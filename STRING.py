# FRASE ENTRE "" OU ' '


print('codar todos os dias')
print("codar todos os dias")
# ASPAS TRIPLAS SERVE PARA QUANDO VAI OCUPAR MAIS DE UMA LINHA
print('''codar todos os dias
para aprender cada vez mais''')

# CARACTERES DE ESCAPE
# \n SERVE PARA PULAR UMA LINHA
print("codar todos os dias \npara aprender cada vez mais")

# \ USADA PARA IGNORAR UM CARACTER
# NESTE CASO ELE IGNORA O " ACRESCENTADO
print("codar todos \"os dias")

# NESTE CASO ELE IGNORA O \ ANTERIOR AO "arquivo e ao "\c""
print('Arquivo localizado em \\c:drive\\arquivo1.txt')

# len - SERVE PARA SABER QUANTOS CARACTERES POSSUI UMA INFORMAÇÃO
nome = "Carol"
print(len(nome))


